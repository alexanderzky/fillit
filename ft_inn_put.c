int	ft_inn_put(int **v, char **map, const char *blocks)
{
	if (blocks[*v[2]] == '.')
	{
		if (map[*v[0]][*v[1] + 1] == '\0')
		{
			if (map[*v[0] + 1] == '\0')
				return (1);
			else
			{
				*v[0] = *v[0] + 1;
				*v[1] = 0;
				*v[2] = 1;
				*v[3] = 0;
			}
		}
		else if (map[*v[0]][*v[1] + 1] != '\0')
		{
			*v[2] = *v[2] + 1;
			*v[1] = *v[1] + 1;
		}
	}
	return (0);
}
